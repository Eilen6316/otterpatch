const GONGWEN = `# 党政机关公文排版(GB/T 9704-2012)

## 版式规范(编辑器可落地的近似)
- 公文标题:二号(22pt)、宋体加粗(小标宋的近似)、居中、单独成段(block=h1 + font=宋体 + size=22 + bold + align=center)
- 正文:三号(16pt)仿宋(font=仿宋 或 仿宋_GB2312、size=16),行距约 28 磅 → lineSpacing=1.5
- 一级标题"一、":三号黑体,不加粗不斜体(黑体本身即强调)
- 二级标题"(一)":三号楷体
- 三级标题"1.":三号仿宋加粗;四级"(1)":三号仿宋
- 成文日期:阿拉伯数字,如"2026年7月2日";正文数字用法从 GB/T 15835(统计数据/编号用阿拉伯数字,固定说法如"十八大"用汉字)

## changeset 惯用法
- 全文正文规范化:setStyle all=true(font=仿宋, size=16, lineSpacing=1.5)一次落定 —— 但先确认标题已用 block 套好层级,否则 all=true 会把标题也拉平
- 层级序号体系必须是:一、→(一)→ 1. →(1),不得混用"1、""(1)."等杂糅体;发现杂糅先用 replaceText 统一序号,再 setStyle 定字体
- 标点:中文公文一律全角标点;"的、地、得"用法与"截止/截至"等易错词顺手修正(replaceText,一处一条)

## 检查清单(动手前过一遍)
1. 标题是否单独成段、居中、无句末标点
2. 层级序号体系是否统一、有无越级
3. 正文字体字号是否统一为三号仿宋;有无宋体/黑体混入正文
4. 数字用法、全角标点、成文日期格式
5. 附件说明("附件:1. ×××")与落款位置

## 反例(不要这样做)
- 用手动加粗+放大冒充标题(要用 block 套真标题样式)
- 用宋体代替仿宋做正文、行距 1.0 挤成一坨
- 一次 edit 塞整篇重排(要一处一条,可分批)`;

const FINANCIAL = `# 财务/经营数据表规范(Excel)

## 检查清单(诊断优先于动手)
1. 勾稽关系:合计=分项之和(先 aggregate 验证再下结论)、期末=期初+本期增减;三表场景:资产=负债+所有者权益、净利润流向未分配利润、现金流期末数=货币资金
2. 合计行/列必须是公式(=SUM(...)),不得硬编码数字 —— 发现硬编码合计,改成公式并在 plan 说明
3. 数字格式:金额千分位(#,##0.00)、百分比两位小数(0.00%)、负数用红色括号((#,##0.00));同列格式必须一致
4. 异常侦测:环比/同比突变(>30% 值得标注)、毛利率为负、费用率异常、重复行、空值断档

## changeset 惯用法
- 改数先查数:任何"改成正确的值"之前,用 read_range 取真实区间、aggregate 验证口径,不许凭样本臆测
- 公式优先:能用 =SUM/=AVERAGE 表达的绝不写死算后值(写死的值在源数据变化后会变成错数)
- 标异常:bgColor 浅红(#ffd6d6)+ 该行加说明,不动原值 —— 分析层与数据层分离
- 大表分批:一次 ops 过多会被截断,plan 里声明"先前 N 行"

## 红线
- 不改原始数据区的历史数值(那是凭证);只修公式错误、加分析层、规范格式
- 改公式前 read_range 看清它引用的区域,别把依赖链改断`;

const CHART = `# 图表选型决策树

## 按问题选型(不是按好看选型)
- 分类间比较 → 柱状图;类别名长/类别多(>8)→ 水平条形并按值排序
- 时间趋势 → 折线图;多序列 ≤5 条,再多就拆分面板
- 构成占比 → 饼/环图(≤6 类,首类从 12 点方向起);>6 类 → 排序条形图
- 构成随时间变化 → 堆叠面积/堆叠柱
- 两变量相关 → 散点图(可加趋势线);分布 → 直方图/箱线
- 目标完成度 → 子弹图/进度条,别用仪表盘浪费空间

## 规范(专业底线)
- 柱状图数值轴必须从 0 起(截断轴会夸大差异);折线图可不从 0 但要标明
- 不用 3D、不用双 Y 轴误导(确需双轴,两轴刻度要可比且分色标注)
- 颜色 ≤6 且色盲友好;强调色只给要说的那一根,其余灰
- 标题写结论("华东区 Q2 销量领先 32%")而非维度名("各区销量")
- 排序要有含义:条形按值排、时间按时间排,别按字母排

## 反例
- 饼图切 12 类、彩虹配色、图例遮数据
- 用面积/气泡大小编码精确比较(人眼对面积不敏感)`;

